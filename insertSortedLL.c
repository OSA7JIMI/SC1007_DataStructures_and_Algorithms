int insertSortedLL(LinkedList *ll, int item)
{
	// add your code here
	ListNode *cur;
	cur = ll->head;
	int i=0;
	if (ll->size==0) {
        insertNode(ll, 0, item);
        return 0;
	}

	while (cur!=NULL) {
        if (item<cur->item)
        {
            insertNode(ll, i, item);
            return i;
        }
        if (item==cur->item) return -1;
        cur=cur->next;
        i++;
	}
	insertNode(ll, ll->size, item);
	return ll->size;


}

